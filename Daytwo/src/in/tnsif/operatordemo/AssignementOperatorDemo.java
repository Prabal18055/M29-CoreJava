package in.tnsif.operatordemo;

public class AssignementOperatorDemo {

	public static void main(String[] args) {
		int a=5;
		int b=4;
		int ans1=(a+=b);
		int ans2=(a-=b);
		int ans3=(a/=b);
		int ans4=(a*=b);
		System.out.println(ans1);
		System.out.println(ans2);
		System.out.println(ans3);
		System.out.println(ans4);

	}

}
